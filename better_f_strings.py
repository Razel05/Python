def kg_to_lbs(kg):
  """Converts kilograms to pounds."""
  return kg * 2.20462

# Example usage
weight_kg = 70
weight_lbs = kg_to_lbs(weight_kg)

print(f"{weight_kg} kg is equal to {weight_lbs:.2f} lbs")


